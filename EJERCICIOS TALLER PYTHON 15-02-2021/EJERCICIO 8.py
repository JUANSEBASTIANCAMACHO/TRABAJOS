#Calcular la fuerza que se debe aplicar a un cuerpo para moverlo en una mesa horizontal,  
# sabiendo que tiene una masa m (kg) y un coeficiente de rozamiento estático  Us.
masa=int(input("ingresar valor de la masa en kilogramos: "))
coeficiente_rozamiento=int(input("ingresar valor de coeficiente de rozamiento estático: "))
fuerza_aplicada=masa*coeficiente_rozamiento
print(fuerza_aplicada)