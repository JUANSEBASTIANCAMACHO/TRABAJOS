#Leer la temperatura en grados Celsius y convertirla a grados Kelvin y a grados Farenheit
temperatura_celsius=int(input("ingresar valor de temperatura en grados celsius: "))
temperatura_kelvin=temperatura_celsius+273.15
temperatura_farenheit=(temperatura_celsius*9/5)+32
print("el valor de la temperatura en kelvin es: ",temperatura_kelvin)
print("el valor de la temperatura en farenheit es: ",temperatura_farenheit)


